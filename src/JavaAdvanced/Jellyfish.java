package JavaAdvanced;

public class Jellyfish {

    private boolean floates;
    private boolean isPoisonous;

    public Jellyfish(boolean floates, boolean isPoisonous) {
        this.floates = floates;
        this.isPoisonous = isPoisonous;
    }

    public boolean isFloates() {
        return floates;
    }

    public boolean isPoisonous() {
        return isPoisonous;
    }
}
